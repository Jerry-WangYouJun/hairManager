package com.hair.model;

public class Product {
	  private  Integer id ; 
	  private String proName;
	  private String type ; 
	  private String subType ; 
	  private String hairType ;
	  private String permed;
	  private String grade ;
	  private String texture ;
	  private Double unit;
	  private Double items;
	  private Double hairLen;
	  private String hairColor ;
	  private String remark;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getSubType() {
		return subType;
	}
	public void setSubType(String subType) {
		this.subType = subType;
	}
	public String getHairType() {
		return hairType;
	}
	public void setHairType(String hairType) {
		this.hairType = hairType;
	}
	public String getPermed() {
		return permed;
	}
	public void setPermed(String permed) {
		this.permed = permed;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getTexture() {
		return texture;
	}
	public void setTexture(String texture) {
		this.texture = texture;
	}
	public Double getUnit() {
		return unit;
	}
	public void setUnit(Double unit) {
		this.unit = unit;
	}
	public Double getItems() {
		return items;
	}
	public void setItems(Double items) {
		this.items = items;
	}
	public Double getHairLen() {
		return hairLen;
	}
	public void setHairLen(Double hairLen) {
		this.hairLen = hairLen;
	}
	public String getHairColor() {
		return hairColor;
	}
	public void setHairColor(String hairColor) {
		this.hairColor = hairColor;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
}
